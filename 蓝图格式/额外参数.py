    每帧耗电_焦耳: int
    本地运输范围: int  # 	本地运输距离的存储方式是100000000*cos(θ)
    星际运输范围_百米: int  # 	星际运输存储方式是百米, 1LY = 2400000M, 无限为 240000000, 也就是100光年
    会从轨道采集器取货: 布尔值
    曲速启用路程M: int  # 	游戏内UI为AU, 1AU = 40000M
    翘曲器必备: 布尔值
    行星运输机起送量百分比: int
    星际运输船起送量百分比: int
    出塔集装层数: int
    采矿速度: int
    行星运输机自动填充: 布尔值
    星际运输船运输机自动填充: 布尔值
    参数类型: str = "物流塔"

    @classmethod
    def 尝试构造(cls, 未解析参数: 额外参数之未解析) -> 额外参数:
        p = 未解析参数.参数
        return cls(
            格子=[物流塔格子.由数组构造(p[i * 6 : i * 6 + 5]) for i in range(32)],
            传送带出口=[
                带子出口.由数组构造(p[192 + i * 4 : 192 + i * 4 + 4]) for i in range(32)
            ],
            每帧耗电_焦耳=p[320],
            本地运输范围=p[321],
            星际运输范围_百米=p[322],
            会从轨道采集器取货=布尔值(p[323]),
            曲速启用路程M=p[324],
            翘曲器必备=布尔值(p[325]),
            行星运输机起送量百分比=p[326],
            星际运输船起送量百分比=p[327],
            出塔集装层数=p[328],
            采矿速度=p[329],
            行星运输机自动填充=布尔值(p[330]),
            星际运输船运输机自动填充=布尔值(p[331]),
        )

    def 转比特流(self) -> bytes:
        params = list(
            chain(
                *[x.转二进制数组() for x in self.格子],
                *[x.转二进制数组() for x in self.传送带出口],
                [
                    self.每帧耗电_焦耳,
                    self.本地运输范围,
                    self.星际运输范围_百米,
                    int(self.会从轨道采集器取货),
                    self.曲速启用路程M,
                    int(self.翘曲器必备),
                    self.行星运输机起送量百分比,
                    self.星际运输船起送量百分比,
                    self.出塔集装层数,
                    self.采矿速度,
                    int(self.行星运输机自动填充),
                    int(self.星际运输船运输机自动填充),
                ],
            )
        )
        params = params + [0] * (2048 - len(params))
        流数据 = bytearray()
        流数据.extend(struct.pack("<H" + "i" * 2048, 2048, *params))
        return 流数据

